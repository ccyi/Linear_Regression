package com.voidalex.MyLinearRegression;

public class DataNode {
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getXY(){
        return this.x * this.y;
    }
}
